    private void btnAdaugareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdaugareActionPerformed
        // TODO add your handling code here:
        if (txtNrCont.getText().isEmpty() || txtCodFiliala.getText().isEmpty() || txtSumaImprumutata.getText().isEmpty()
                || txtSumaRestituit.getText().isEmpty() || txtTipCont.getText().isEmpty() || txtRataDobanzii.getText().isEmpty()
                || txtNrClient.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "completati toate campurile!");
        } else {
            Ipoteca ipt = new Ipoteca();
            
            ipt.setNumar_cont(txtNrCont.getText());
            ipt.setCod_filiala_conducere(Integer.parseInt(txtCodFiliala.getText()));
            ipt.setSuma_imprumutata(Float.parseFloat(txtSumaImprumutata.getText()));
            ipt.setSuma_de_restituit(Float.parseFloat(txtSumaRestituit.getText()));
            ipt.setTip_cont(txtTipCont.getText());
            ipt.setTip_ipoteca(txtTipIpoteca.getText());
            ipt.setRata_dobanzii(Float.parseFloat(txtRataDobanzii.getText()));
            ipt.setNr_client(Integer.parseInt(txtNrClient.getText()));
            String mesaj = ibo.addIpoteca(ipt);
            JOptionPane.showMessageDialog(null, mesaj);
            clean_field();
            listIpoteca();

        }
    }//GEN-LAST:event_btnAdaugareActionPerformed

