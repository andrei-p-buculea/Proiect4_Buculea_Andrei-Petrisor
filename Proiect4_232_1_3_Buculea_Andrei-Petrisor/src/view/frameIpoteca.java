        txtNrCont.setText(tbIpoteca.getValueAt(selection, 0) + "");
        txtCodFiliala.setText(tbIpoteca.getValueAt(selection, 1) + "");
        txtSumaImprumutata.setText(tbIpoteca.getValueAt(selection, 2) + "");
        txtSumaRestituit.setText(tbIpoteca.getValueAt(selection, 3) + "");
